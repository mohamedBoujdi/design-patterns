package org.example;

interface Sort {
    public void sort(Integer[] arr);
}
class BubbleSort implements Sort {
    public void sort(Integer[] arr) {
        System.out.println("BubbleSort");
        int n = arr.length;
        for (int i = 0; i < n - 1; i++) {
            for (int j = 0; j < n - i - 1; j++) {
                if (arr[j] > arr[j + 1]) {
                    int temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                }
            }
        }

    }
}
